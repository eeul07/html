package ex0422;

public class MovieVO {
	private int seq_mno;
    private String title;
    private String actor;
    
	public int getSeq_mno() {
		return seq_mno;
	}
	public void setSeq_mno(int seq_mno) {
		this.seq_mno = seq_mno;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getActor() {
		return actor;
	}
	public void setActor(String actor) {
		this.actor = actor;
	}
} // class